package local.example.sportnews

import android.os.Bundle
import android.widget.ImageView
import android.widget.TextView
import androidx.appcompat.app.AppCompatActivity
import com.bumptech.glide.Glide

class DetailActivity : AppCompatActivity() {

    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContentView(R.layout.activity_detail)

        val sportsTitle = findViewById<TextView>(R.id.title_detail)
        val sportsImage = findViewById<ImageView>(R.id.sport_image_detail)

        sportsTitle.text = intent.getStringExtra("title")

        Glide.with(this).load(intent.getIntExtra(
            "image_resource",
            0
        ))
            .into(sportsImage)
    }
}
